//Show the total number of characters in a user's message.


public class Task8 {
  
    public static void main(String[] args) {
        // ইউজারের মেসেজ
        String message = "How are you?";

        // ক্যারেক্টার সংখ্যা গণনা
        int characterCount = message.length();

        // ফলাফল দেখানো
        System.out.println("Total characters in message: " + characterCount);
    }
}
  
